package model;

public class BeanCookbook {
    private int cookbook_id;
    private String cookbook_name;
    private String cookbook_ingredient;
    private String cookbook_step;
    private String cookbook_image;

    public int getCookbook_id() {
        return cookbook_id;
    }

    public void setCookbook_id(int cookbook_id) {
        this.cookbook_id = cookbook_id;
    }

    public String getCookbook_name() {
        return cookbook_name;
    }

    public void setCookbook_name(String cookbook_name) {
        this.cookbook_name = cookbook_name;
    }

    public String getCookbook_ingredient() {
        return cookbook_ingredient;
    }

    public void setCookbook_ingredient(String cookbook_ingredient) {
        this.cookbook_ingredient = cookbook_ingredient;
    }

    public String getCookbook_step() {
        return cookbook_step;
    }

    public void setCookbook_step(String cookbook_step) {
        this.cookbook_step = cookbook_step;
    }

    public String getCookbook_image() {
        return cookbook_image;
    }

    public void setCookbook_image(String cookbook_image) {
        this.cookbook_image = cookbook_image;
    }
}
